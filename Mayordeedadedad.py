# Nombre: Programa para definir si la persona es mayor o menor de edaad.

# Entradas:
#   Edad: Edad del usuario


# Salidas:
#   Confirmacion: Comprobar si es mayor o no

# Proceso: Definir si el usuario es mayor de edad o no.

edad = int(input("Digite su edad: "))
if edad >= 18:
    print("Ust es mayor de edad")
else:
    print("Ust es menor de edad")
