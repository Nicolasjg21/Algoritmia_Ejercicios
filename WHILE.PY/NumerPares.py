# Nombre: Programa para realizar un conteo automatico hasta 20 de 2 en 2.

# Entradas:
# num: Variable hecha para almacenar la suma de 2 en 2 constantemente hasta llegar a 20.

# Salidas:
#   Lista de numeros: Una vez ejecutada la funcion se mostrara la lista del 2 al 20 automaticamente y un respectivo mensaje.

# Proceso: Por medio de una suma automatica, el usuario ejecutara el programa y se le mostrara la lista creada.


num = 0
while num <= 20:
    num = num + 2
    print(num)
print("Numeros pares del 2 al 20")
