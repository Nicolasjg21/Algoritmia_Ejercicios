# Nombre: Programa para realizar un conteo automatico hasta 10.

# Entradas:
# i: Variable hecha para almacenar la suma de 1 constantemente hasta llegar a 10.

# Salidas:
#   Lista de numeros: Una vez ejecutada la funcion se mostrara la lista del 1 al 10 automaticamente y un respectivo mensaje.

# Proceso: Por medio de una suma automatica, el usuario ejecutara el programa y se le mostrara la lista creada.


i = 0
while i <= 10:
    i = i + 1
    print(i)
print("La lista muestra un conteo hasta 10")
