# Nombre: Programa para relizar un conteo regresivo apartir del 10.

# Entradas:
# i: Variable encargada de almacenar la resta hasta llegar a 0.

# Salidas:
#  Creacion de lista: Se le mostrara al usuario una lista de numeros del 10 al 0 automaticamente.
# Proceso: Una vez ejecutado el programa, se mostrara el conteo dÂ¿regresivo de 10 a 0.


i = 10
while i >= 1:
    i = i - 1
    print(i)
print("Su lista fue creada")
